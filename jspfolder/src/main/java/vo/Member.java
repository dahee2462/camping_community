package vo;

public class Member {
	private int mno;
	private String mid;
	private String mpw;
	private String mnickNm;
	private String mname;
	private int mbirth;
	private String mphone;
	private char mgender;
	private String memail;
	private String mrdate;
	
	public int getMno() {
		return mno;
	}
	public void setMno(int mno) {
		this.mno = mno;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getMpw() {
		return mpw;
	}
	public void setMpw(String mpw) {
		this.mpw = mpw;
	}
	public String getMnickNm() {
		return mnickNm;
	}
	public void setMnickNm(String mnickNm) {
		this.mnickNm = mnickNm;
	}
	public String getMname() {
		return mname;
	}
	public void setMname(String mname) {
		this.mname = mname;
	}
	public int getMbirth() {
		return mbirth;
	}
	public void setMbirth(int mbirth) {
		this.mbirth = mbirth;
	}
	public String getMphone() {
		return mphone;
	}
	public void setMphone(String mphone) {
		this.mphone = mphone;
	}
	public char getMgender() {
		return mgender;
	}
	public void setMgender(char mgender) {
		this.mgender = mgender;
	}
	public String getMemail() {
		return memail;
	}
	public void setMemail(String memail) {
		this.memail = memail;
	}
	public String getMrdate() {
		return mrdate;
	}
	public void setMrdate(String mrdate) {
		this.mrdate = mrdate;
	}
}
